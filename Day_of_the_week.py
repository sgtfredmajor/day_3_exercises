dowNumber = int(input("Please input a number from (0-6) for day of the week: "))

if dowNumber == 0:
    print( "Sunday")
elif dowNumber == 1:
    print( "Monday")
elif dowNumber == 2:
    print( "Tuesday")
elif dowNumber == 3:
    print( "Wednesday")
elif dowNumber == 4:
    print( "Thuesday")
elif dowNumber == 5:
    print( "Friday")
elif dowNumber == 6:
    print( "Saturday")
else:
    print("Error please input a number from (0-6)")
